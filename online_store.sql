CREATE PROCEDURE `insert_order`(
	_cart_id bigint unsigned,
	_user_id double,
	_drop_point_id bigint unsigned,
	_payment_method_id bigint unsigned
)
BEGIN
	UPDATE cart_details cd
		JOIN items i ON cd.item_id = i.item_id 
			SET cd.purchase_price = ROUND((i.price - i.price * (i.discount / 100)), 2),
				cd.normal_purchase_price = i.price
		WHERE cd.cart_id = _cart_id;
	INSERT INTO orders (user_id, cart_id, status_id, drop_point_id, payment_method_id, is_paid, is_completed)
	VALUES(_user_id, _cart_id, 1, _drop_point_id, _payment_method_id, 0, 0);
	UPDATE carts SET is_active = 0 where cart_id =_cart_id and user_id = _user_id and is_active = 1;

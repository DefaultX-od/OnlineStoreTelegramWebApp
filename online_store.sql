CREATE PROCEDURE `get_cart`(_cart_id bigint)
BEGIN
	SELECT
		cd.item_id,
		i.name,
		CASE 
        	WHEN cd.normal_purchase_price IS NULL THEN i.price
        	ELSE cd.normal_purchase_price
    	END AS normal_price,
   		CASE 
        	WHEN cd.purchase_price IS NULL THEN ROUND((i.price - i.price * (i.discount / 100)), 2)
        	ELSE cd.purchase_price
    	END AS discount_price,
		i.album,
		cd.quantity
	FROM carts c 
	JOIN cart_details cd ON c.cart_id = cd.cart_id
	JOIN items i ON cd.item_id = i.item_id
	WHERE c.cart_id = _cart_id;
END ;;
